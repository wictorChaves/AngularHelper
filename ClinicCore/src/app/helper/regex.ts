export class Regex {

    interger(){
        return /^-?(0|[1-9]\d*)?$/;
    }

    brlPositive(){
        return /^([0-9]+),{1}([0-9]+)$|^[0-9]+$/;
    }

    brl(){
        return /^(-[0-9]+|[0-9]+),{1}([0-9]+)$|^(-|[0-9]+)[0-9]*$/;
    }

    date(){
        return /([12]\d{3}-(0[1-9]|1[0-2])-(0[1-9]|[12]\d|3[01]))/;
    }

    time(){
        return /^([01]?[0-9]|2[0-3]):[0-5][0-9]$/;
    }

}
